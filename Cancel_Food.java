    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(cust_id.getText().trim().isEmpty())
        {
            custid_err.setText("*This field is required");
            custid_err.setForeground(Color.red);
        }
        else{
        Connection con = DBConnect.connect();
        String type = String.valueOf(jComboBox1.getSelectedItem());

            try {
                String sql = "select * from order_food where cust_id = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setInt(1, Integer.valueOf(cust_id.getText()));
                ResultSet rs = ps.executeQuery();
                order_view.setModel(DbUtils.resultSetToTableModel(rs));
                if(!rs.next())
                {
                    JOptionPane.showMessageDialog(null, "There is no customer with given ID.Please Enter details Again","Display Message",JOptionPane.INFORMATION_MESSAGE);
                    cust_id.setText("");
                }
                con.close();
                
            } 
            catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

