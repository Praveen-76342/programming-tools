    private void confirm_cancellationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirm_cancellationActionPerformed
        // TODO add your handling code here:
        if(cust_id.getText().trim().isEmpty() && order_id.getText().trim().isEmpty())
        {
            custid_err.setText("*This field is required");
            custid_err.setForeground(Color.red);
            orderid_err.setText("*This field is required");
            orderid_err.setForeground(Color.red);
        }
       else if(cust_id.getText().trim().isEmpty())
        {
            custid_err.setText("*This field is required");
            custid_err.setForeground(Color.red);
            orderid_err.setText("");
            orderid_err.setForeground(Color.red);
        }
       else if(order_id.getText().trim().isEmpty())
        {
            custid_err.setText("");
            custid_err.setForeground(Color.red);
            orderid_err.setText("*This field is required");
            orderid_err.setForeground(Color.red);
        }
        else{
        int ord_id = Integer.valueOf(order_id.getText());
        String type = String.valueOf(jComboBox1.getSelectedItem());
        if(type.equals("Breakfast") ||type.equals("Lunch-Veg") || type.equals("Lunch-NonVeg") || type.equals("Snacks") || type.equals("Dinner-Veg") || type.equals("Dinner-NonVeg"))
        {
            try {
            Connection con =DBConnect.connect();
            String sql = "delete from order_food where order_id = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1,ord_id);
            ps.executeUpdate();
            
        } catch (Exception e) {
                System.out.println(e);
        }
        }
        
    }
    }//GEN-LAST:event_confirm_cancellationActionPerformed

